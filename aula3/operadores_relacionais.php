<h2>Operadores de relacionais</h2>
<hr>
<p>
    São operadores utilizados para verificar a relação 
    (maior, menor, difeente) entre valores.
</p>

<strong>Operadores</strong>
<ol>
    <li>>   | Maior</li>
    <li><   | Menor</li>
    <li>>=  | Maior Igual</li>
    <li><=  | Menor Igual</li>
    <li>==  | Igual</li>
    <li>!=  | Diferente</li>
    <li>=== | Idêntico - compara valor e tipo</li>
    <li>!== | Não idêntico - compara valor e tipo</li>
</ol>

<strong>Exemplos</strong>

<pre>
    &lt;?php
        $reposta = 10 == 10;
        var_dump($resposta);
    ?&gt;
</pre>

<?php
    $resposta = 10 == 10;
    var_dump($resposta);
?>

<pre>
    &lt;?php
        $reposta = 10 === "10";
        var_dump($resposta);
    ?&gt;
</pre>

<?php
    $resposta = 10 === "10";
    var_dump($resposta);
?>